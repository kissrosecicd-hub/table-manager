tables.forEach(t => {
  const colIds = t.cols.map(() => genId());
  const records = t.records.map((row, i) => {
    const rec = { id: String(i + 1) };
    t.cols.forEach((col, j) => { rec[colIds[j]] = row[j]; });
    return rec;
  });

  data.tables.push({
    id: genId(),
    name: t.name,
    columns: t.cols.map((name, i) => ({ id: colIds[i], name, required: name === 'id' })),
    records,
    createdAt: now,
    updatedAt: now,
  });
});

